// switch case in javaScript

// variable
let month = "march";

// switch case condition to check the month
switch (month) {
  case "january":
    console.log("month is january");
    break;
  case "febraury":
    console.log("month is febraury");
    break;
  case "march":
    console.log("month is march");
    break;
  case "april":
     console.log("month is april");
    break;
  case "may":
    console.log("month is may");
    break;
  case "june":
    console.log("month is june");
    break;
  
}
