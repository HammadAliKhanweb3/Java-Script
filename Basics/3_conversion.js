// conversion in javascript

// variable
let score = 50;
// converting number string
let stringNumber = String(score);

console.log(stringNumber);
// checking type of variable
console.log(typeof stringNumber);

// converting string to number
let valueof = "50";

// converting string into number
let Numbervalueof = Number(valueof);

console.log(Numbervalueof);

console.log(typeof Numbervalueof);

let value = "50AbC";
// converting string to number when number is mixed with string
let Numbervalue = Number(value);

console.log(Numbervalue);
console.log(typeof Numbervalue);

let i = 1;
// converting 0 and 1 in boolean
let booleani = Boolean(i);

console.log(booleani);

console.log(typeof booleani);

